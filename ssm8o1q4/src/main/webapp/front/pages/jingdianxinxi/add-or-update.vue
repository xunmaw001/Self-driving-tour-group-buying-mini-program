							await this.$api.update(`jingdianxinxi`, this.ruleForm);
						}else{
							await this.$api.add(`jingdianxinxi`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`jingdianxinxi`, this.ruleForm);
					}else{
						await this.$api.add(`jingdianxinxi`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
